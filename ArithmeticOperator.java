public class ArithmeticOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;
        int sum = a + b + c;
        int substraction = c - b;
        int multiplication = a * b;
        int division = b / a;
        int mod = b % a;

        System.out.println("The sum of a, b and c is: " + sum);
        System.out.println("The subtraction of c and b is: " + substraction);
        System.out.println("The multiplication of a and b is: " + multiplication);
        System.out.println("The division of b by a is: " + division);
        System.out.println("The modulus of b and a is: " + mod);
    }
}
     